package cad.lab3.nomer4_22;

public class App {

    public static void main(String[] args) {
        System.out.println("N\t\t10*N\t\t100*N\t\t1000*N");
        for (int i = 1; i <= 5; i++) {
            System.out.println(i + "\t\t" + 10 * i + "\t\t\t" + 100 * i + "\t\t\t" + 1000 * i);
        }
    }
}
